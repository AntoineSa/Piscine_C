int		ft_atoi(char *str)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (*str == '\t' || *str == '\n' || *str == '\r' ||
			*str == '\v' || *str == '\f' || *str == ' ')
		str++;
	if (*str == 43 || *str == 45)
		str++;
	if (*str < 48 || *str > 57)
		return (res);
	while (*str <= 57 && *str >= 48)
	{
		i++;
		res = res * 10 + (*str - 48);
		str++;
	}
	if (*(str - (i + 1)) == '-')
		res = -res;
	return (res);
}

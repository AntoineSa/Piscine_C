#include "sudoku.h"

int		ft_find_empty(int *r, int *c, char **tab)
{
	int i;
	int j;

	i = 0;
	j = 0;
	while (i < 9)
	{
		while (j < 9)
		{
			if (tab[i][j] == '.')
			{
				*r = i;
				*c = j;
				return (1);
			}
			j++;
		}
		j = 0;
		i++;
	}
	return (0);
}

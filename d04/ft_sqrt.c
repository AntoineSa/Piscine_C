int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 0;
	res = 0;
	while (nb != res)
	{
		i++;
		res = i * i;
		if (res > nb)
		{
			i = 0;
			res = nb;
		}
	}
	return (i);
}

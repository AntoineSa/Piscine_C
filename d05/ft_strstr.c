char	*ft_strstr(char *str, char *to_find)
{
	int i;
	int j;

	i = 0;
	while (str[i] != '\0')
	{
		j = 0;
		while (to_find[j] == str[j + i] || to_find[j] == '\0')
		{
			if (to_find[j] == '\0')
			{
				str += i;
				return (str);
			}
			j++;
		}
		i++;
	}
	return (0);
}

char	*ft_strcapitalize(char *str)
{
	char *cur;

	cur = str;
	if (*str >= 97 && *str <= 122)
		*str = *str - 32;
	while (*cur != '\0')
	{
		if ((*(cur - 1) == ' ' || *(cur - 1) == '+' ||
				*(cur - 1) == '-' || *(cur - 1) == ',')
				&& (*cur >= 'a' && *cur <= 'z'))
			*cur = *cur - 32;
		if (((*(cur - 1) >= 97 && *(cur - 1) <= 122)
			|| (*(cur - 1) > 64 && *(cur - 1) <= 90))
			&& (*cur >= 65 && *cur <= 90))
			*cur = *cur + 32;
		cur++;
	}
	return (str);
}
